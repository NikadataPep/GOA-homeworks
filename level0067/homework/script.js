function digitalRoot(n) {
  /// გავაგრძელოთ იტერაცია სანამ n არ გახდება ციფრი
  while (n >= 10) {
    let sum = 0;
    //// გადავუყვეთ რიცხვის თითოეულ ციფრს
    while (n > 0) {
      //<> მივიღოთ n-ის ბოლო ციფრი
      sum += n % 10;

      //\ მოვაშოროთ n-ის ბოლო ციფრი რათა იტერაცია გაგრძელდეს შემდეგ ციფრზე
      n -= n % 10; //| აშორებს ბოლო ციფრს n-დან
      n /= 10;
    }
    n = sum;
  }
  return n;
}

console.log(digitalRoot(229));
